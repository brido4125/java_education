package ch3;

public class OperatorEx13 {
    public static void main(String[] args) {
        char c1 = 'a';
        char c2 = (char)(c1 + 1);
        char c3 = 'a' + 1;

        System.out.println("c1 = " + c1);
        System.out.println("c2 = " + c2);
        System.out.println("c3 = " + c3);
    }
}
